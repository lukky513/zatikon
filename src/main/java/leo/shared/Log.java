package leo.shared;

// imports

import org.tinylog.Logger;

import java.text.DateFormat;
import java.text.SimpleDateFormat;
import java.util.Date;

public class Log {

    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private static final DateFormat format = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");


    /////////////////////////////////////////////////////////////////
    // Get the time
    /////////////////////////////////////////////////////////////////
    private static String getTime() {
        return format.format(new Date());
    }


    /////////////////////////////////////////////////////////////////
    // Log an error
    /////////////////////////////////////////////////////////////////
    public static void error(String text) {
        Logger.error(text);
    }


    /////////////////////////////////////////////////////////////////
    // Log activity
    /////////////////////////////////////////////////////////////////
    public static void activity(String text) {
        Logger.debug(text);
    }


    /////////////////////////////////////////////////////////////////
    // Log game actions
    /////////////////////////////////////////////////////////////////
    public static void game(String text) {
        Logger.info(text);
    }


    /////////////////////////////////////////////////////////////////
    // Log game actions
    /////////////////////////////////////////////////////////////////
    public static void alert(String text) {
        Logger.warn(text);
    }


    /////////////////////////////////////////////////////////////////
    // Chat
    /////////////////////////////////////////////////////////////////
    public static void chat(String text) {
        Logger.info("CHAT: " + text);
    }


    /////////////////////////////////////////////////////////////////
    // System
    /////////////////////////////////////////////////////////////////
    public static void system(String text) {
        Logger.info(text);
    }


}
